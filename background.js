// Function to randomize user agents
function randomizeUserAgent() {
    return userAgents[Math.floor(Math.random() * userAgents.length)];
}
function clearCookiesPeriodically() {
    chrome.cookies.getAll({}, function (cookies) {
        for (var i = 0; i < cookies.length; i++) {
            chrome.cookies.remove({ url: "http://" + cookies[i].domain + cookies[i].path, name: cookies[i].name });
            chrome.cookies.remove({ url: "https://" + cookies[i].domain + cookies[i].path, name: cookies[i].name });
        }
    });
}

// Initialize switches state
// At the top of background.js
let isCookiesSwitchEnabled = false;
let isUserAgentSwitchEnabled = false;

// Load saved state when starting
chrome.storage.sync.get(['cookiesEnabled', 'userAgentEnabled'], function (result) {
    isCookiesSwitchEnabled = result.cookiesEnabled || false;
    isUserAgentSwitchEnabled = result.userAgentEnabled || false;
    updateDynamicRules();
});

// Rest of your background.js code...
chrome.runtime.onMessage.addListener(function (request, sender, sendResponse) {
    if (request.type === "cookiesSwitch") {
        isCookiesSwitchEnabled = request.state;
        if (isCookiesSwitchEnabled) {
            cookieClearIntervalId = setInterval(clearCookiesPeriodically, 1000 * 15);
        } else {
            // Clear the interval if it exists
            if (cookieClearIntervalId) {
                clearInterval(cookieClearIntervalId);
                cookieClearIntervalId = null;
            }
        }
    } else if (request.type === "userAgentSwitch") {
        isUserAgentSwitchEnabled = request.state;
    }
    updateDynamicRules();
});

function updateDynamicRules() {
    let rulesToAdd = [];
    let ruleIdsToRemove = [];

    // User-Agent wijzigingsregel
    if (isUserAgentSwitchEnabled) {
        rulesToAdd.push({
            id: 1,
            priority: 1,
            action: {
                type: "modifyHeaders",
                requestHeaders: [{
                    operation: "set",
                    header: "User-Agent",
                    value: randomizeUserAgent(),
                }],
            },
            condition: {
                resourceTypes: ["main_frame", "sub_frame"],
            },
        });
    } else {
        ruleIdsToRemove.push(1);
    }

    // Cookie blokkeringsregel
    if (isCookiesSwitchEnabled) {
        rulesToAdd.push({
            id: 2,
            priority: 2,
            action: {
                type: "modifyHeaders",
                requestHeaders: [{
                    operation: "remove",
                    header: "cookie",
                }, {
                    operation: "remove",
                    header: "Cookie",
                }, {
                    operation: "remove",
                    header: "cookies",
                }, {
                    operation: "remove",
                    header: "Cookies"
                }]
            },
            condition: {
                resourceTypes: ["main_frame", "sub_frame"],
            },
        });
    } else {
        ruleIdsToRemove.push(2);
    }

    // Update de regels
    chrome.declarativeNetRequest.updateDynamicRules({
        removeRuleIds: [1,2],
        addRules: rulesToAdd,
    });
}

// Initial call to set up the rules based on initial states
updateDynamicRules();


